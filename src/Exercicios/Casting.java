package Exercicios;

public class Casting {
	
	public static void main (String[]args) {
		
		int a = 5;
		int b = 2;
		double resultado;
		
		resultado = (double) a / b; 
		
		System.out.println("Casting Resultado:"+ resultado);
	
		resultado = (int) a;
				
		System.out.println("Casting 2:"+ resultado);
		
	}
		

}
